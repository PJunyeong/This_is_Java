package verify.Exercise17;

public class Printer {
	// 메소드를 정적 메소드로 만든
	static void println(int input) {
		System.out.println(input);
	}
	static void println(boolean input) {
		System.out.println(input);
	}
	static void println(double input) {
		System.out.println(input);
	}
	static void println(String input) {
		System.out.println(input);
	}
}
