package sec02.exam04_float_double;

public class FloatDoubleExample {

	public static void main(String[] args) {
		double var1 = 3.14;
		//float var2 = 3.14;
		//Java의 실수 기본 타입은 double이므로 F를 붙여 float를 나타낸다.
		float var3 = 3.14F;
		int var6 = 3000000;
		double var7 = 3e6;
		float var8 = 3e8f;
		double var9 = 2e-3;
		
		double var10 = 0.1234567890123456789;
		float var11 = 0.1234567890123456789F;
		System.out.println(var10);
		System.out.println(var11);
		// float 타입은 가수 23비트 표현
		
	}

}
